              <p>3. Based on the hard edges you&rsquo;ve found, mark the initial boundaries of your bioregion &mdash; and&nbsp;voi la!&nbsp;&nbsp;You should be looking at a potential map of your bioregion!And now you&rsquo;re ready to test it.</p>
              <h3 id="testing-your-bioregion">Testing your bioregion</h3>
              <p>How do you know if the bioregion you&rsquo;ve mapped, is, well, a&nbsp;bioregion?</p>
              <p></p>
              <p>After finding the hard boundaries of our region, we can also check &ldquo;soft&rdquo; internal boundaries like ecosystems and and human cultural boundaries, to see if it passes tests like the following.</p>
              <p></p>
              <p>While our bioregional boundaries will not shrink at this point - they may grow!&nbsp;</p>
              <p></p>
              <p>Self Sufficiency Test:Is this bioregional area large enough to be practically self-sufficient? (While no one area may ever be&nbsp;truly&nbsp;self-sufficient, a majority of cultural and geographic connection should take place within a bioregion.)</p>
              <p></p>
              <ul>
                  <li>
                      <p>Where does the food and power come from?&nbsp;</p>
                  </li>
                  <li>
                      <p>What resources are generated in the region?&nbsp;</p>
                  </li>
                  <li>
                      <p>Who are people talking to, working with, what are the economic hubs, and connection points?&nbsp;</p>
                  </li>
                  <li>
                      <p>Where does the water come from and go?</p>
                  </li>
                  <li>
                      <p>Are there natural disasters that affect areas broader than what you mapped?</p>
                  </li>
                  <li>
                      <p>How do the areas in the region work together? Do you feel there is a sense of shared identity?&nbsp;</p>
                  </li>
              </ul>
              <p>Water Test: Are there any other bodies of water that feed into this area? Or water in this area that feeds into other regions? Look for layers of interconnection and interdependence.</p>
              <p>‚ùóAs you answer the questions above, you may find reason to expand the bounds of your bioregion to encompass more neighboring areas. &nbsp;If so, do it! &nbsp;(There&rsquo;s no right answer &mdash; and anybody can make their own map, based on their own responses to these questions!)</p>
              <p></p>
              <p>If your area performed well in these tests, you are looking at your bioregion!</p>
              <h3>Follow Up and Next Steps</h3>
              <p></p>
              <p>As a wrap-up, try the following prompts.</p>
              <p></p>
              <ol>
                  <li>
                      <p>&nbsp;What are the boundaries of your bioregion?</p>
                  </li>
              </ol>
              <p>You can draw them on paper, on a digital map, or describe them however suits you.&nbsp;üîß<a href="https://geojson.io/">https://geojson.io/</a></p>
              <ol start="2">
                  <li>
                      <p>Do you have a possible name for your bioregion / ecoregion? (Remember &mdash; there are no right or wrong answers!)</p>
                  </li>
                  <li>
                      <p>Now that you have a bioregion, what features and layers would you add to the map &mdash; things that are important or special to you, or things that you depend on?</p>
                  </li>
                  <li>
                      <p>Do you feel this map might be useful to groups, organizers or people living in it? &nbsp;How or why? &nbsp;(If not, how could it be different?)</p>
                  </li>
                  <li>
                      <p>What other hard edges, soft edges, or cultural layers in your bioregion are you curious about, but didn&rsquo;t yet have time to explore?</p>
                  </li>
              </ol>
              <p></p>
              <p>Congratulations! &nbsp;You now have a bioregion &mdash; from the largest drainage basin or coastline, all the way down to your watershed &mdash; and maybe even a name for it!</p>
