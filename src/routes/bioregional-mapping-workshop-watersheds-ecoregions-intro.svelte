              <h3>What is a bioregion?</h3>
              <p></p>
              <p>A bioregion is a land and water territory whose limits are naturally defined by topographic features (such as mountain ranges and rivers) and biological features (such as plants, animals, humans and entire ecosystems).&nbsp;</p>
              <p></p>
              <p>Bioregions are the natural countries of the planet, and the largest framework in which&nbsp;shared experience based on place&nbsp;makes sense. &nbsp; Bioregions transcend arbitrary political boundaries, and are interconnected with one another.</p>
              <p></p>
              <p>Bioregional mapping can also serve as a launching point for organizing, planning, sustainability projects, and more!</p>
              <p></p>
              <p>Did you know?&nbsp;</p>
              <p></p>
              <p>At the 1992 United Nations Rio Conference on the Environment, bioregional &nbsp;mapping was identified as a best&nbsp;practice for locally-based sustainability planning.</p>
              <p></p>
              <p>Now, we&rsquo;ll start mapping our own bioergion. &nbsp;You can work as a team, individually, or in small groups. &nbsp;At the end of the exercise, you&rsquo;ll have a chance to share your work with others making these maps too!</p>
